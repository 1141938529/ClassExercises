import turtle
turtle.showturtle()
turtle.pensize(2)
turtle.penup()
turtle.goto(-39,48)
turtle.write("(-39,48)", font=("Arial", 12, "normal"))
turtle.pendown()
turtle.color("red")
turtle.goto(50,-50)
turtle.color("black")

turtle.write("(50,-50)",font=("Arial", 12, "normal"))
turtle.hideturtle()
turtle.done()